package ex_21072024;

public class LabCode132 {
    public static void main(String[] args) {


        int i =10;
        System.out.println("----for while loop----");
        while (i<10)
        {
            System.out.println(i);
            i++;
        }

        System.out.println("_________");
        System.out.println("----for do while loop----");
        do {
            System.out.println(i);
            i++;
        }while (i<10);
    }
}

